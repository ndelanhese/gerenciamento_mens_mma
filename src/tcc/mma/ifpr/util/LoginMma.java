    private void jbconActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbconActionPerformed
        int v = 1;
        int bd = bd();

        if (bd == 0) {
            JOptionPane.showMessageDialog(null, "Sem conex√£o com o banco!", "ERRO", NORMAL);
            txtuser.setText("");
            txtpass.setText("");
            txtuser.disable();
            txtpass.disable();
        } else {

            if (txtuser.getText().isEmpty() && txtpass.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os dados", "ERRO", NORMAL);
                txtuser.requestFocus();
                v = 0;
            } else {

                if (txtuser.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Preencha o usu√°rio", "ERRO", NORMAL);
                    txtuser.requestFocus();
                    v = 0;
                }
                if (txtpass.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Preencha a senha", "ERRO", NORMAL);

                    txtpass.requestFocus();
                    v = 0;
                }
            }

            if (v == 1) {
                try {
                    Connection con = FabricaDeConexoes.criarConexaoParaMySQL();
                    UsuarioDAO daouser = new UsuarioDAO(con);
                    Usuario user = daouser.retrieve(txtuser.getText().toLowerCase());

                    if (pass(txtpass.getText()).equals(user.getPass())) {
                       

                        setVisible(false);
                        String nome[] = user.getNome().split(" ");
                        String nomeFinal = "";
                        if (nome.length > 2) {
                            nomeFinal = nome[0].concat(" " + nome[nome.length - 1]);
                        } else {
                            nomeFinal = user.getNome();
                        }
                        main.lbluser.setText(nomeFinal);
                        main.lblcon.setText("Conectado");
                        main.tipoUsuario = user.getTipoUsuario();

                        telaLoading();

                    } else {
                        JOptionPane.showMessageDialog(null, "Senha Inv√°lida!", "ERRO", NORMAL);
                        txtpass.setText("");
                        txtpass.requestFocus();
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Credenciais n√£o encontradas!", "ERRO", NORMAL);
                    txtuser.setText("");
                    txtpass.setText("");
                    txtuser.requestFocus();

                }
            }
        }

    }//GEN-LAST:event_jbconActionPerformed

